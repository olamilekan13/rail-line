            <p>Two sets of fare are available, namely Published Fares and Implemented Fares. Published Fares are the original price while Implemented Fares are the actual selling price. Implemented Fares will not be higher than Published Fares. Child Ticket fares are about half the Adult Implemented Fares.</p>
            <p>Fares are set in Naira fare being adjusted monthly subject to the prevailing exchange rate. Adjusted HKD fares will be announced on the first day of every month. For tickets sold in HKDs, the fare is the valid Implemented Fare for the current month which may not be the same as the fare on the date of travel.</p>
            <p>Example: A ticket for a train departing from Ikeja Station to Iju Station on 5 February is sold at #700 on 23 January. Due to subsequent appreciation of NAIRA   , the HKD fare is adjusted upward to $265 on 1 February, which means a passenger buying a ticket from 1 February will pay an extra #5 for the same journey.</p>
            <p>Fares are different for different Classes of Travel: Second Class, First Class, Premium Class and Business Class are available. Premium Class and Business Class are only available on some Mainland trains.</p>
            <p>All fares are subject to confirmation by Lagos Metro Station. </p>
            
               
