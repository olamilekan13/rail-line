        <p class="title">PART I PRELIMINARY</p>
        
        <p class="sub-title">1. CITATION</p>
        
        <p>These by-laws may be cited as the Mass Transit Railway By-laws.</p>
        
        <p class="sub-title">2. INTERPRETATION</p>
        
        <p>In these by-laws, unless the context otherwise requires- 
            <br/>"concessionary ticket" means a ticket issued at a special fare or subject to special conditions, privileges or restrictions in the conditions of issue; 
            <br/>"conditions of issue"  means the conditions of issue of tickets for travel upon the railway published from time to time by or on behalf of the Corporation and posted at its stations; 
            <br/>"fare" means the fare payable by any passenger to whom or on whose behalf a ticket is issued by or on behalf of the Corporation for use on the railway; 
            <br/>"first class ticket" means a ticket bearing on it the number "1" and issued at the prevailing first class full single or return fare for the journey being made or to be made or a smart card which has had the authorization code for first class travel recorded on it by an automatic processing device immediately prior to the ticket holder entering the first class compartment; 
            <br/>"goods" includes livestock and any other animal; 
            <br/>"official"  means any person duly authorized to act on behalf of the Corporation; 
            <br/>"paid area" means that part of the railway premises-  (a) set aside for the use of fare-paying passengers, and  (b) provided with ticket gates, barriers or turnstiles for the purposes of entry or exit, and includes the trains when in service;  
            <br/>"passenger"  means a person to whom or on whose behalf a ticket is issued and who is lawfully within the paid area; 
            <br/>"railway" means the railway as defined in the Ordinance excluding the North-west Railway as defined in the Ordinance; 
            <br/>"railway premises"  means the railway premises other than railway premises of the North-west Railway; 
            <br/>"railway premises of the North-west Railway"  means that part of the railway premises as defined in the Ordinance belonging to or appertaining to the North-west Railway but excludes any carriageway of the North-west Railway not designated by the Commissioner for the purpose of the definition of "road" under section 2 of the Road Traffic Ordinance (Cap. 374); 
            <br/>"surcharge"  means such amount, not exceeding an amount equal to fifty times the maximum adult single fare at the time the surcharge is to be paid, as may be specified in the conditions of issue;  
            <br/>"ticket"  means any form of ticket, card, pass or permit from time to time issued by the Corporation, or by persons duly authorized by the Corporation, for travel on the railway, or regarded under by-law 10(1A) as a ticket issued by or on behalf of the Corporation; 
            <br/>"ticket office"  means an office operated by or on behalf of the Corporation which is duly authorized to issue a ticket; 
            <br/>"train" means any train (or carriage or compartment thereof) owned by or in the possession of the Corporation.</p>
        
        <p class="title">PART II TRESPASS AND DAMAGE TO RAILWAY PREMISES</p>
        
        <p class="sub-title">3. ADMISSION</p>
        
        <p>(1) The Corporation reserves the right to refuse to admit any person onto the railway premises or any part thereof at any time.<br />
        (2) The Corporation may open or close any entrance to or exit from any station or platform or any part of the paid area or any other part of the railway premises at such times as it considers expedient without incurring any liability to any person for any loss or damage however arising as a result thereof.</p>
        
        <p class="sub-title">3A. AUTHORIZED CROSSING PLACES AND CONDITIONS OF CROSSING</p>
        
        <p>The Corporation may by notice stipulate periods or times at which a crossing place for animals, persons, motor vehicles, motorcycles, bicycles or other conveyances may be used over the railway premises or any part thereof and the conditions upon which such crossing place may be used.</p>
        
        <p class="sub-title">4. TRESPASS</p>
        
        <p>(1) No person, unless otherwise authorized by the Corporation, shall-<br />
        (a) enter into or upon any part of the railway premises, other than those parts clearly defined by means of notices, indicators and other directions for the use of persons using, or intending to use the railway; or<br />
        (b) enter or leave such parts other than by proper use of such gates, barriers or turnstiles (if any) provided for such entry or exit.<br />
        (2) (Repealed)<br />
        (3) A person shall be responsible for any injury, loss or damage caused to the Corporation’s property or staff or to any other person or property by such person or by any article or animal brought by him onto the railway premises and he shall indemnify the Corporation from and against any liability to any other person resulting therefrom.</p>
        
        <p class="sub-title">4A. NO VEHICLES, ANIMALS, ETC. TO BE BROUGHT ACROSS THE RAILWAY PREMISES</p>
        
        <p>No person shall pass or attempt to bring, pass, drive or conduct any motor vehicle, bicycle, motorcycle or other similar conveyance or any handcart, barrow or similar conveyance or any thing including animals across the railway premises or any part thereof at any time except by notice published by or on behalf of the Corporation under this by-law nor shall any such person omit to shut or refasten any gate, door, chain or barrier as soon as he and any conveyance, animal or other thing has passed through the same.</p>
        
        <p class="sub-title">4B. UNAUTHORIZED STRUCTURES</p>
        
        <p>No person shall cause, permit or suffer any kind of building or structure to be constructed or erected upon or remain upon the railway premises without the written authority of the Corporation.</p>
        
        <p class="sub-title">5. DAMAGE TO RAILWAY PREMISES, TRAINS, PLANT AND EQUIPMENT</p>
        
        <p>No person shall improperly touch, use, meddle, damage or otherwise interfere with-<br />
        (a) any machine or equipment, or any part thereof, used or employed in or upon any part of the railway premises;<br />
        (b) any locomotive, train, carriage, truck or any other conveyance or any equipment thereon used or employed on or in connection with the railway;<br />
        (ba) any tracks, rails and supporting system including fastenings, fixtures, baseplates, plinths, sleepers and ballast;<br />
        (bb) any gate, door, chain, wall, fence, barrier or other erection constructed or erected in or upon any part of the railway premises;<br />
        (bc) any building and structure constructed or erected in or upon on any part of the railway premises; or<br />
        (c) any electrical plant, overhead wiring or other form of electrical installation or equipment of any nature whatsoever used or employed in or upon any part of the railway.</p>
        
        <p class="sub-title">6. SEWAGE ETC. NOT TO BE PLACED ON RAILWAY PREMISES</p>
        
        <p>No person shall cause, permit or suffer-<br />
        (a) any sewage, drainage or other offensive matter to flow onto or enter or be placed on any part of the railway premises;<br />
        (b) any kind of construction materials, construction plant or equipment to be deposited on or otherwise come upon and remain upon or pass across the railway premises except with the written authority of the Corporation; or<br />
        (c) the water or contents of any reservoir, tank, ponds, duct or water or other container under the control of the Corporation or forming part of or being upon the railway premises or any part thereof to be used, abstracted or polluted in any way.</p>
        
        <p class="sub-title">7. RUBBISH ETC. NOT TO BE THROWN ON OR FROM RAILWAY PREMISES</p>
        
        <p>No person shall deposit or throw, or cause to be deposited or thrown, at, on or from the railway premises any glass, stone, missile or any rubbish or other offensive or waste matter.</p>
        
        <p class="sub-title">8. IMPROPER USE OF EMERGENCY EQUIPMENT</p>
        
        <p>No person, unless otherwise authorized by the Corporation, shall activate any emergency or safety device on the railway premises save for the express purpose for which the same is provided and in accordance with the instructions printed thereon.</p>
        
        <p class="sub-title">9. WRONGFULLY ENTERING OR LEAVING TRAINS</p>
        
        <p>(1) No person shall enter or leave or attempt to enter or leave any train after the doors have commenced to close. <br />
        (2) No person shall interfere with any doors or gates within the railway premises including any train doors and platform screen doors.</p>
        
        <p class="title">PART III TICKETS</p>
        
        <p class="sub-title">10. CONDITIONS OF ISSUE OF TICKETS</p>
        
        <p>(1) All tickets issued by or on behalf of the Corporation are issued subject to these by-laws and to the conditions of issue.<br />
        (1A) All tickets which were issued by or on behalf of KCRC before the Merger Date and which continue to have effect from the Merger Date until their expiry by virtue of section 3(2)(a) of the Kowloon-Canton Railway Corporation (Suspension of Bylaws) Bylaw 2007 shall from the Merger Date be regarded as tickets issued by or on behalf of the Corporation subject to these by-laws and to the conditions of issue.<br />
        (2) Any person to whom or on whose behalf a ticket is issued or who otherwise acquires a ticket shall be deemed to have knowledge of and to have agreed to these by-laws and to the conditions of issue.</p>
        
        <p class="sub-title">11. FARES</p>
        
        <p>The fares appearing in notices, lists or tables published from time to time by the Corporation and posted at stations are the authorized fares for travel upon the railway.</p>
        
        <p class="sub-title">12. TICKETS CONTAIN NO WARRANTY OR ACCEPTANCE OF LIABILITY</p>
        
        <p>(1) The Corporation does not warrant that a passenger will be conveyed on any particular train or that any train shall depart or arrive at a particular time or times or that the issue of a ticket will be completed before the departure of any train and the Corporation will not be liable to any person for any loss or damage arising from any delay or detention caused by the alteration, suspension or withdrawal of the Corporation���s train services (or a part thereof) for any reason whatsoever.<br />
        (2) The Corporation may at its discretion and without being liable to any person for any loss or damage caused thereby- <br />
        (a) suspend or discontinue the issue of tickets whether issued from a ticket office or by an automatic vending machine; <br />
        (b) despatch any train from a station before the arrival of any other train without affording the passengers in the arriving train an opportunity to alight and board the departing train; and <br />
        (c) suspend, discontinue or otherwise withdraw all or any train services from any station on any day or suspend, discontinue or withdraw the running of any train or alter the times of departure or arrival of any train.</p>
        
        <p class="sub-title">12A. INSUFFICIENT CLASS ACCOMMODATION</p>
        
        <p>(1) The Corporation shall not be liable for any failure to carry a passenger in any first class compartment of any train where there is insufficient first class accommodation. In case of such insufficient accommodation, a ticket may be refunded before the passenger in respect of whom the ticket has been issued travels on any train.<br />
        (2) Where there is insufficient first class accommodation on any train for which a ticket has been issued, a passenger holding a first class ticket may travel in standard class accommodation without the right to claim a refund of the appropriate difference in fare.</p>
        
        <p class="sub-title">13. COMPLIANCE WITH CONDITIONS OF ISSUE</p>
        
        <p>(1) No person shall enter, leave or travel upon or attempt to enter, leave or travel upon the railway otherwise than in accordance with the conditions of issue.<br />
        (2) Every passenger shall leave the paid area within such period as may be prescribed in the conditions of issue failing which he shall, where he has no reasonable excuse for remaining within the paid area after the expiry of such period, pay the maximum fare for a single direction journey on the railway as specified in the conditions of issue.</p>
        
        <p class="sub-title">14. ENTRY AND TRAVEL PROHIBITED WITHOUT TICKET</p>
        
        <p>No person shall, without lawful authority or reasonable excuse-  (a) enter or leave, or attempt to enter or leave, the paid area; or  (b) travel or attempt to travel upon any part of the railway, without first paying his fare and obtaining a valid ticket appropriate to the circumstances of his intended journey and using that ticket by inserting it into an automatic gate or by using it in an appropriate manner over the electronic sensor of an automatic gate as may be required by the conditions of issue of such ticket on entering or leaving the paid area or otherwise producing it and delivering it up to an official.</p>
        
        <p class="sub-title">14A. FAILURE TO PAY FARES, ETC.</p>
        
        <p>No person shall, prior to leaving the paid area, fail or refuse to pay any fare, surcharge or other sum leviable in accordance with these by-laws.</p>
        
        <p class="sub-title">15. TRAVEL WHERE TICKET IS LOST, DAMAGED OR EXPIRED</p>
        
        <p>(1) If a person (other than a person who is under the age of 3 years) is within the paid area- <br />
        (a) without a ticket; <br />
        (aa) with a ticket which is invalid for travel in the carriage or compartment of the train in which the ticket holder is travelling, and in the case of a passenger travelling in a first class compartment, with a smart card which has not had the authorization code for first class travel encoded on it by an automatic processing device immediately prior to the ticket holder entering a first class compartment; <br />
        (b) with a ticket improperly damaged, altered or interfered with or whose coded data has been improperly altered, erased or damaged either wholly or in part; <br />
        (c) with a ticket that has expired; or <br/>(d) with a concessionary ticket when he does not meet any of the conditions upon which the ticket is issued, he shall be regarded as not having paid his fare and shall be liable both to pay a surcharge and to deliver up his ticket (if any) to an official.<br />
        (2) For the purposes of paragraph (1), a ticket shall expire in such circumstances as are provided for in the conditions of issue.<br />
        (2A) For the purpose of this by-law, passengers occupying seats or standing including those in corridors or gangways in a first class compartment shall be regarded as travelling in a first class compartment.<br />
        (3) Any person who has paid a surcharge or delivered up his ticket pursuant to the provisions of this by-law shall be entitled to apply in writing to the Chief Executive Officer (or his appointed nominee) for a review of the circumstances in which he became liable to a surcharge or to deliver up his ticket and the Chief Executive Officer (or his appointed nominee) upon the conclusion of such review may at his absolute discretion reject such application or may authorize repayment of the whole or any part of the surcharge or the remaining value on the delivered up ticket. <br />
        (4) Without prejudice to paragraph (1), any passenger holding a ticket who travels beyond a station for which his ticket is valid shall be liable to pay the excess fare specified in the conditions of issue.</p>
        
        <p class="sub-title">16. PASSENGERS SHOULD EXAMINE TICKETS AND CHANGE</p>
        
        <p>(1) A person purchasing a ticket should examine his ticket and any change tendered before leaving any ticket office and the Corporation shall not be liable for any error or omission not drawn to its attention at the time of issue of the ticket.<br />
        (2) A person using an automatic vending machine shall insert not less than the appropriate fare in Lagos Staionlegal tender for the purchase of a ticket and a person is entitled to a refund of the amount in excess of the appropriate fare inserted into an automatic vending machine.<br />
        (2A) No person shall insert or attempt to insert into any automatic vending machine or coin change machine any coin object or thing other than Lagos Staionlegal tender in the denominations identified by notice on the machine as suitable for use in that machine.<br />
        (3) The amount (including a nil amount) from time to time encoded on a ticket shall be conclusive evidence of the amount paid in respect of such ticket and of the value (if any) remaining in such ticket.<br />
        (4) Any passenger holding a concessionary ticket shall, if requested by an official to do so, produce to that official acceptable proof of his entitlement to a concessionary ticket.</p>
        
        <p class="sub-title">17. HANDING IN OF TICKETS</p>
        
        <p>(1) Unless otherwise stated in the conditions of issue, all tickets shall remain the property of the Corporation and no person shall, without reasonable excuse, fail or refuse at the end or sooner determination of his journey, or series of journeys in the case of a multiple journey or stored value ticket, to deliver up his ticket to the Corporation.<br />
        (2) A passenger within the paid area or within the railway premises after having immediately gained exit from the paid area must produce any ticket for checking, inspection or verification at any time upon demand by any official.<br />
        (3) Any person who contravenes paragraph (2) shall be regarded as not having paid his fare and shall be liable to pay a surcharge.<br />
        (4) No person other than a person specifically authorized by or on behalf of the Corporation to do so shall sell, attempt to sell, offer for sale or invite other persons to purchase any ticket issued by or on behalf of the Corporation.</p>
        
        <p class="sub-title">18. DAMAGING ETC. A TICKET</p>
        
        <p>(1) No person shall improperly do anything to or with a ticket whereby- <br />
        (a) the coded data thereon is erased wholly or in part, or is otherwise altered or interfered with; or <br />
        (b) the ticket is otherwise damaged.<br />
        (2) No person shall use or attempt to use a ticket which has been improperly altered, damaged or interfered with for the purpose of entering or leaving the paid area or travelling upon the railway.</p>
        
        <p class="sub-title">19. (REPEALED)</p>
        
        <p class="sub-title">20. EXCHANGES AND REFUNDS</p>
        
        <p>(1) A ticket may be refunded or exchanged only at the discretion of the Corporation or its authorized agents, and the refund or exchange may be subject to the deduction of an administration charge prescribed in the conditions of issue.<br />
        (2) The form of any refund shall be at the discretion of the Corporation.<br />
        (3) The Corporation shall not be liable to issue a ticket in replacement of a lost or unused ticket nor will it be liable to make a refund in respect of any such lost or unused ticket or in respect of a surcharge charged to any passenger in accordance with these by-laws.</p>
        
        <p class="title">PART IV CONDUCT OF PASSENGERS</p>
        
        <p class="sub-title">21. COMPLIANCE WITH NOTICES</p>
        
        <p>(1) Every person while on the railway premises shall comply with these by-laws and with all notices, indicators and all reasonable directions and requests of any official.<br />
        (2) Without prejudice to paragraph (1), if an official determines that a train is full, no person shall enter or remain therein if directed by such official not to do so.</p>
        
        <p class="sub-title">22. FEET NOT TO BE PLACED ON SEATS</p>
        
        <p> No person shall place his feet on any seat in any part of the railway premises.</p>
        
        <p class="sub-title">23. SMOKING PROHIBITED</p>
        
        <p>No person shall smoke or carry a lighted pipe, cigar or cigarette or naked flame in any form in any other part of the railway premises where smoking is prohibited by notice.</p>
        
        <p class="sub-title">23A. FIRE HAZARD</p>
        
        <p>No person shall place or throw any lighted cigarette end, match, tobacco, liquid, substance or any other thing upon the railway premises in a manner which constitutes or is likely to constitute a fire hazard.</p>
        
        <p class="sub-title">24. SPITTING, ETC. PROHIBITED</p>
        
        <p>No person shall- <br/>(a) spit on any part of the railway premises; or <br/>(b) place or throw any litter upon the railway premises, except into receptacles provided for that purpose.</p>
        
        <p class="sub-title">25. PASSENGERS NOT TO CAUSE A NUISANCE</p>
        
        <p> No person shall conduct himself on any train or in any part of the railway premises so as to cause a nuisance or annoyance to other passengers.</p>
        
        <p class="sub-title">26. MUSICAL INSTRUMENTS ETC.</p>
        
        <p> PROHIBITED No person, unless authorized by the Corporation, shall sing or dance, play or perform with any musical instrument in any part of the railway premises.</p>
        
        <p class="sub-title">26A. PLAYING RADIOS, CASSETTES, ETC.</p>
        
        <p> No person, unless authorized in writing by the Corporation, shall play or use or attempt to play or use any radio, cassette, compact disc player, record player, portable wireless television, or any other similar device upon any part of the railway premises which shall generate noise.</p>
        
        <p class="sub-title">27. PROHIBITION ON TAKING CERTAIN LUGGAGE, ETC. AND CONSUMPTION OF FOOD OR BEVERAGE</p>
        
        <p>No person shall- <br/>(a) bring into or upon the railway premises any luggage, article or other thing which cannot be carried or otherwise accommodated on the railway without risk of injury to any person or damage to any railway property or without causing a nuisance or inconvenience to other persons using the railway; or <br/>(b) consume or attempt to consume any food or beverage (whether alcoholic or non-alcoholic) within the paid area (except any train for the carriage of passengers to Lagos Staionfrom any other part of China or vice versa by the Corporation or by third parties).</p>
        
        <p class="sub-title">28. ANIMALS PROHIBITED ON RAILWAY PREMISES </p>
        
        <p>No person shall, unless the Corporation in its sole discretion allows or permits, bring any animal or other livestock into or upon any part of the railway premises (provided that this restriction shall not apply to a guide dog accompanying a blind person).</p>
        
        <p class="sub-title">28A. IMPROPER OPERATION OF EQUIPMENT, ETC.</p>
        <p>(1) No person, unless authorized by the Corporation, shall- <br />
        (a) operate, move, or interfere with - <br />
        (i) any mechanical, electrical, electronic, telecommunications or other appliance owned by or under the control of the Corporation upon the railway premises; or <br />
        (ii) any switch, lever or other device operating or controlling any mechanical, electrical , electronic, telecommunications or other appliance owned by or under the control of the Corporation upon the railway premises, the proper use of any automatic gate and telephones; <br />
        (b) tamper with or wilfully impede or interfere with the operation of any mechanical, electrical, electronic, telecommunications or other appliance owned by or under the control of the Corporation; <br />
        (c) ascend or descend, or attempt to ascend or descend, by means of any escalator other than in such manner and order as is directed by the Corporation; <br />
        (d) travel, or attempt to travel, upon any escalator or moving platform in a direction other than the direction in which it is moving; <br />
        (e) sit upon any escalator or moving platform or any handrail or any part thereof; <br/>(f) open or attempt to open, any gate or doors leading to or from a platform or a paid area at a station upon the railway premises; or <br />
        (g) hold or possess keys, passes or entry cards to any door or gate inside the railway premises and shall immediately surrender the same to the Corporation as soon as the same come to their possession.<br />
        (2) In cases of accident or other emergency, a person may operate, move, or work any switch, lever or other device or mechanical, electrical, electronic, telecommunications or other appliance upon or near which is displayed a notice that it is intended to be operated in cases of accident or emergency.</p>
        
        <p class="sub-title">28B. CLIMBING BARRIERS, TURNSTILES, ETC. PROHIBITED</p>
        
        <p>No person shall climb or jump on or over any wall, fence, barrier, turnstile or post in any part of the railway premises.</p>
        
        <p class="sub-title">28C. ENTRANCE OR EXIT BY IMPROPER MEANS AND QUEUING</p>
        <p>(1) A person shall- <br />
        (a) enter or attempt to enter any train through any door thereof to or from the platform at stations; <br />
        (b) wait at the platform of a station for the arrival of a train or wait inside a train for the arrival at the platform of a station; or <br />
        (c) gain access from the platform of a station to a train or gain access from a train to the platform of a station, only at such time and location and in such manner as an official or other authorized persons shall reasonably provide and require.<br />
        (2) No person except a member of the staff of the Corporation or an official shall enter or leave or attempt to enter or leave any train whilst it is in motion or (except in case of accident or other emergency) between stations or otherwise than at the side of the train adjoining the platform appointed for passengers to enter or leave the train.<br />
        (3) The Corporation may establish queues on the railway premises for the purpose of regulating the access to services and facilities provided on or in the vicinity of the railway premises.<br />
        (4) Every person desirous of availing himself of any such service or facility described in paragraph (3) shall, upon notice or request by a member of the staff of the Corporation or an official, take up position in the rear of one of such queues established pursuant to paragraph (3) and move forward in an orderly and regular manner, and obey the reasonable instructions of any member of the staff of the Corporation or official regulating such queues.</p>
        
        <p class="sub-title">28D. ENTRANCE TO TRAINS BY IMPROPER MEANS OR OVERLOADING</p>
        
        <p>When an official determines, in his absolute discretion, that a train or part thereof contains the full load, no additional person shall enter or remain or attempt to enter or remain therein if directed by the official not to do so.</p>
        
        <p class="sub-title">28E. ENTRY TO RESTRICTED AREA</p>
        
        <p> Unless authorized by the Corporation, no person shall enter or remain in any area declared by the Corporation, by notices, signs or any other manner as will reasonably indicate the same as restricted area.</p>
        
        <p class="sub-title">28F. INTOXICATION</p>
        
        <p> No person in a state of intoxication resulting from consuming or abusing alcohol, medicine or drug or in an unfit condition as determined by an official in his absolute discretion shall enter or remain or attempt to enter or remain upon the railway premises.</p>
        
        <p class="sub-title">28G. IMPROPER DRESSING</p>
        
        <p> No person whose dress or clothing is in a condition liable to soil or injure the dress or clothing or personal effects of any other person in or upon a railway premises shall enter or attempt to enter a train or a railway premises unless an official in his absolute discretion grants permission to such a person.</p>
        
        <p class="sub-title">28H. ABUSIVE LANGUAGE</p>
        
        <p>(1) No person shall at any time while upon the railway premises- <br />
        (a) use any threatening, abusive, obscene or offensive language, or behave in a riotous, disorderly, indecent or offensive manner; <br />
        (b) paint, write, draw or affix any word, representation or character upon, or wilfully soil or defile or break, cut, scratch, tear, spray, deface or damage any part of the railway premises including any train or any of the fittings, furniture, decorations or equipment, or any publication, notice, list, time-table, advertisement, sign, figure or letter, or remove or detach any such article or object; <br />
        (c) damage any property upon the railway premises; <br />
        (d) molest any person or wilfully interfere with the comfort or convenience of any such person; or <br />
        (e) without the prior approval in writing of the Corporation, and subject to such terms and conditions as the Corporation may impose, use any voice recording or video recording or camera equipment for the conduct of interviews or taking or making of films or videos.<br />
        (2) A person offending this by-law is liable to the Corporation for the amount of the damage done to any property or personal injury caused to the officers of the Corporation or damage or injury suffered by any other person without prejudice to any penalty incurred by the contravention of this bylaw.</p>
        
        <p class="sub-title">28I. FLYING MATERIALS ENDANGERING OPERATIONS</p>
        <p> No person shall cause, permit or suffer any kite, balloon, model or other thing to fly or pass into or over any part of the railway premises including any tunnel or in or on to the air space or any overhead line above the railway premises which may endanger proper railway operation.</p>
        
        <p class="title">PART V HAWKERS, LOITERERS AND BILL POSTING</p>
        
        <p class="sub-title">29. SOLICITING FOR LUGGAGE HANDLING ALMS, ETC.</p>
        
        <p> No person shall on the railway premises solicit alms or advantage of any description.</p>
        
        <p class="sub-title">30. HAWKING PROHIBITED</p>
        
        <p> No person, unless authorized in writing by the Corporation, shall sell or expose or offer for sale any goods, wares or services in or upon any railway premises, and sections 86, 86A, 86C and 86D of the Public Health and Municipal Services Ordinance (Cap 132) shall apply to an offence under this by-law as if such offence were a hawker offence within the meaning of section 83 of that Ordinance.</p>
        
        <p class="sub-title">31. LOITERING PROHIBITED No person shall loiter in or about any part of the railway premises.</p>
        
        <p class="sub-title">32. BILL POSTING ETC. PROHIBITED</p>
        
        <p>No person shall, unless authorized in writing by the Corporation-  <br/>(a) post, stick, paint or write or cause to be posted, stuck, painted or written any placard, bill, advertisement or any other matter; or <br/>(b) distribute any book, leaflet or other printed matter or any sample or other article, on any part of the railway premises.</p>
        
        <p class="sub-title">32A. UNAUTHORIZED DISPLAY OF MATERIALS FOR THE PURPOSE OF ADVERTISEMENT, ETC.</p>
        
        <p>No person while upon the railway premises shall, except by permission of a member of the staff of the Corporation or an authorized person, display or exhibit any printed, written or pictorial matter or any article for the purpose of advertisement or publicity.</p>
        
        <p class="title">PART VI MOTOR VEHICLES ON RAILWAY PREMISES</p>
        
        <p class="sub-title">33. MOTOR VEHICLES NOT TO BE LEFT ON RAILWAY PREMISES</p>
        
        <p>Except with the written permission of the Corporation, no person shall leave or cause to be left any motor car or other vehicle on any part of the railway premises or on any station approach road or entrance being under the control of the Corporation.</p>
        
        <p class="sub-title">34. DEALING WITH VEHICLES LEFT ON RAILWAY PREMISES</p>
        
        <p>(1) The Corporation may in such manner as it thinks fit remove and detain any motor car or other vehicle in respect of which a contravention of by-law 33 is committed and may charge the owner or driver thereof all costs and expenses occasioned by and incidental to such removal and detention without prejudice to any penalty incurred by the contravention of that by-law.<br />
        (2) If a vehicle detained under paragraph (1) is not claimed and removed and all costs and expenses are not paid within 3 days after its detention, the Corporation shall where practicable except in the case of emergency serve on the registered owner (as defined in the Road Traffic Ordinance (Cap 374)) of the vehicle a notice informing him- <br />
        (a) of the detention of the vehicle and the place of detention; and <br />
        (b) that, unless the vehicle is removed from the place of detention on payment of any costs and charges within 14 days after the service of the notice on him, the vehicle shall become the property of the Corporation free from the rights of any person and may be disposed of by the Corporation by sale or otherwise.<br />
        (3) If a vehicle is not removed in accordance with the notice served under paragraph (2) or, if the service of such notice is impracticable, within 14 days of the date of its first detention, the vehicle shall become the property of the Corporation free from the rights of any person and may be disposed of by the Corporation by sale or otherwise as it thinks fit.<br />
        (4) If, within 6 months after the day on which a vehicle is sold pursuant to paragraph (3), any person satisfies the Corporation that at the time the vehicle became the property of the Corporation by virtue of that paragraph, he was the owner of the vehicle, the Corporation shall pay to such person the balance of the proceeds of sale after deducting any costs and charges of removal and detention and any reasonable charges incurred by the Corporation in respect of the sale of the vehicle.<br />
        (5) A notice under paragraph (2) may be served personally or by post.<br />
        (6) For the purpose of this by-law "vehicle" (車輛) includes the contents of a vehicle and any load carried by a vehicle.</p>
        
        <p class="sub-title">35. VEHICLE DRIVERS TO COMPLY WITH SIGNS ETC.</p>
        
        <p>Vehicle drivers shall while in or upon any part of the railway premises obey all traffic signs and signals and the reasonable instructions and directions of any official.</p>
        
        <p class="sub-title">36. DANGEROUS DRIVING</p>
        
        <p>No person shall drive any motor car or other vehicle through, into or upon any part of the railway premises at a rate of speed or in a manner liable to involve danger to others.</p>
        
        <p class="sub-title">37. VEHICLES PROHIBITED ON CERTAIN PARTS OF RAILWAY PREMISES</p>
        
        <p>No person shall drive any motor car or other vehicle upon or along any part of the railway premises set apart for the exclusive use of pedestrians.</p>
        
        <p class="title">PART VII FIREARMS, ETC.</p>
        
        <p class="sub-title">38. FIREARMS</p>
        
        <p>No person not being a member of the Armed Forces, a police officer, a member of the Customs and Excise Department or an officer of the Independent Commission Against Corruption shall carry or have with him on any railway premises any arms or ammunition.</p>
        
        <p class="sub-title">39. DANGEROUS GOODS</p>
        
        <p>No person not being an official duly authorized by the Corporation in that behalf shall bring onto any part of the railway premises any substance or other thing which is subject to the provisions of the Dangerous Goods Ordinance (Cap 295).</p>
        
        <p class="title">PART VIIA CARRIAGE OF LUGGAGE</p>
        
        <p class="sub-title">39A. CONDITIONS OF CARRIAGE OF LUGGAGE</p>
        
        <p>The Corporation shall only accept luggage for carriage on the railway subject to these by-laws and the conditions of carriage of luggage set out from time to time in notices published by the Corporation.</p>
        
        <p class="sub-title">39B. SOLICITING FOR HANDLING OF LUGGAGE</p>
        
        <p>No person other than an official or a person licensed by the Corporation to do so shall solicit for engagement in or for the handling or moving or transport of any luggage or any item thereof for reward.</p>
        
        <p class="title">PART VIIB CARRIAGE OF GOODS</p>
        
        <p class="sub-title">39C. CONDITIONS OF ACCEPTANCE OF GOODS</p>
        
        <p>(1) The Corporation shall accept goods for carriage on the railway or storage subject to these by-laws and the conditions of carriage of goods set out from time to time in notices published by the Corporation.<br />
        (2) The Corporation reserves the right at its absolute discretion to refuse any goods for carriage on the railway, and to open or examine such goods and remove them to a safe place. The Corporation may, without being liable, remove or dispose of any goods which might in its opinion cause injury or nuisance to persons or damage to property.</p>
        
        <p class="sub-title">39D. SOLICITING FOR HANDLING OF GOODS</p>
        
        <p> No person other than a person licensed by the Corporation to do so shall solicit for engagement in or for the handling or moving or transport of any goods or item thereof for reward.</p>
        
        <p class="title">PART VIII LOST PROPERTY</p>
        
        <p class="sub-title">40. LOST PROPERTY</p>
        
        <p> A person who finds any lost property in or upon any part of the railway premises shall report to an official at the nearest station, and no person other than an official shall remove from any train or carriage any property lost or left behind therein, save for the purpose of handing over the same forthwith to an official.</p>
        
        <p class="sub-title">41. DISPOSAL OF LOST PROPERTY</p>
        
        <p>(1) All lost property which comes into the possession of the Corporation shall be dealt with as follows- <br />
        (a) perishable, noxious or otherwise offensive goods or articles may be disposed of by the Corporation as soon as practicable after they have come into its possession by sale or otherwise as it sees fit; <br />
        (b) identification and travel documents, certificates or any other document which the Corporation shall regard to be of an important or confidential nature may be disposed of by the Corporation within such time they have come into its possession and in such manner as it sees fit; and <br />
        (c) all other goods or articles shall be retained by the Corporation for a period of 1 month after they have come into its possession and, if at the end of that period they remain unclaimed, they shall be deemed to become the property of the Corporation free of all other rights and encumbrances, and it may dispose of them by sale or otherwise as it sees fit.<br />
        (2) If within a period of 6 months of any sale or disposal by the Corporation under paragraph (1)(a) or (b) the former owner or the person formerly entitled to the beneficial ownership of the goods can establish his ownership to the satisfaction of the Corporation, he shall be paid, subject to his providing the Corporation with an indemnity in retention as the Corporation may reasonably require, the proceeds of sale, if any, less all expenses incurred by the Corporation of and incidental to the sale or disposal.<br />
        (3) Save as provided in paragraph (2), the Corporation shall incur no liability whatsoever to any person in respect of lost property as bailees or otherwise and no claim for damages or compensation shall be brought against it by any person in respect of the same.</p>
        
        <p class="title">PART VIIIA CROSS-BOUNDARY RESTRICTED AREAS</p>
        
        <p class="sub-title">41A. INTERPRETATION</p>
        
        <p>In this Part, unless the context otherwise requires – <br />
         "authorized employee" (獲授權僱員) means an employee of the Corporation or other person authorized in writing by the Corporation for the purposes of any by-law in this Part; <br />
        "cross-boundary restricted area" (過境限制區) means for the purposes of this Part an area declared under by-law 41B to be a cross-boundary restricted area, such area being an area referred to in sections 34(1A)(b) and 35(6) of the Ordinance; <br />
        "permit" (許可證) means any permit issued under this Part or any permit that is regarded under by-law 41F(2) as a permit issued by the Corporation under this Part; <br />
         "permit holder" (許可證持有人) means – a) in relation to a pool permit, the authorized employee to whom the permit is issued and includes any person using a pool permit by authority of such authorized employee; and b) in relation to a standard permit, the person in whose name the permit is issued; <br />
        "pool permit" (共用許可證) means a permit issued to an authorized employee under by-law 41F(1); <br />
        "standard permit" (標準許可證) means a permit issued to a person under by-law 41F(1).</p>
        
        <p class="sub-title">41B. GAZETTE NOTICE RELATING TO CROSS-BOUNDARY RESTRICTED AREAS</p>
        <p>(1) The Corporation may, by notice published in the Gazette, declare any area within the railway premises including the paid area to be a cross-boundary restricted area.<br />
        (2) A declaration under paragraph (1) may declare any area to be a cross-boundary restricted area either absolutely or on specified days or during specified hours in any day.</p>
        
        <p class="sub-title">41C. DEMARCATION OF CROSS-BOUNDARY RESTRICTED AREAS</p>
        
        <p>The Corporation shall cause the boundaries of or entrances to every cross-boundary restricted area to be demarcated by signs or in such other manner as will reasonably indicate the cross-boundary restricted area to members of the public who might enter that area.</p>
        
        <p class="sub-title">41D. PLAN SHOWING CROSS-BOUNDARY RESTRICTED AREAS</p> 
        <p>(1) The Chief Executive Officer may prepare and certify a plan delineating the area and boundaries of any cross-boundary restricted area and shall from time to time prepare and certify a new plan in substitution therefor and may from time to time endorse on such plan or substituted plan any amendment thereto and shall certify such endorsement.<br />
        (2) The Chief Executive Officer shall certify any plan or amendments thereto under paragraph (1) by endorsing the certificate on the plan.<br />
        (3) Every plan certified under this by-law shall be kept in the head office of the Corporation and a copy shall be available for public inspection at the Station Manager’s office at the Hung Hom Station.</p>
        
        <p class="sub-title">41E. PROHIBITION OF PERSONS WITHOUT PERMIT IN CROSS-BOUNDARY RESTRICTED AREAS</p>
        
        <p>Subject to the provisions of this Part, no person shall enter or remain in a cross-boundary restricted area unless he has on his person a valid permit issued to him in respect of that area.</p>
        
        <p class="sub-title">41F. CONDITIONS OF ISSUE OF TYPE OF PERMITS</p>
        
        <p>(1) Subject to by-law 41G, the Corporation may issue – <br />
        (a) a pool permit to any authorized employee free of charge; and <br />
        (b) a standard permit to any other person on an application being made therefor in accordance with conditions specified from time to time by the Corporation and on payment of the prescribed fee set out in Schedule 1, and the pool permit or standard permit, whichever is appropriate, shall be in such form and be issued subject to such conditions as may be specified by the Corporation from time to time.<br />
        (2) Without prejudice to the generality of the power of the Corporation to cancel any permit under by-law 41H, all permits which were issued by the KCRC before the Merger Date and which continue to have effect from the Merger Date until their expiry by virtue of section 3(2)(b) of the Kowloon-Canton Railway Corporation (Suspension of Bylaws) Bylaw 2007 shall from the Merger Date be regarded as permits issued by the Corporation under this Part.</p>
        
        <p class="sub-title">41G. POWER TO REFUSE ISSUE OF PERMITS</p>
        <p> The Corporation may refuse to issue a permit if it appears to it that the person requiring the permit or for whom it is required – <br/>(a) is, for any reason related to or connected with the security of the railway, not a fit person to have access to a cross-boundary restricted area; or <br/>(b) has no valid or sufficient reason for having access to a cross-boundary restricted area.</p>
        
                        <p class="sub-title">41H. CANCELLATION OF PERMITS</p>
                        <p>The Corporation</p>
            <p>(a) shall cancel a permit if it appears to it that the permit holder –  <br/>(i) is, for any reason related to or connected with the security of the railway, not a fit person to have access to a cross-boundary restricted area; or  <br/>(ii) has no valid or sufficient reason for having access to a cross-boundary restricted area; and <br/>(b) may cancel a permit   <br/>(i) on the ground that any condition subject to which it was issued has been contravened;   <br/>(ii) on the ground that the permit holder or his employer has contravened any of these by-laws; or  <br/>(iii) in any case where it is satisfied that the permit has been lost, destroyed or defaced.</p>
        
        <p class="sub-title">41I. NOTIFICATION OF CANCELLATION OF PERMITS</p>
        
        <p>When a permit is cancelled under by-law 41H, the Corporation shall notify the permit holder and, if the Corporation thinks fit, the employer (if any) of the permit holder accordingly.</p>
        
        <p class="sub-title">41J. SURRENDER OF PERMIT BY EMPLOYEE</p>
        
        <p>A permit holder shall, upon receipt of a notice under by-law 41I, save where the notice relates to a permit which has been lost or destroyed, forthwith surrender his permit to –  <br/>(a) the Corporation;   <br/>(b) an authorized employee specified in the notice for the purpose; or  <br/>(c) the employer of the permit holder if specified in the notice for the purpose.</p>
        
        <p class="sub-title">41K. SURRENDER OF PERMIT BY EMPLOYER</p>
        
        <p>An employer to whom a permit has been surrendered by virtue of a notice under by-law 41I shall forthwith surrender that permit to the Corporation or an authorized employee specified in the notice.</p>
        
        <p class="sub-title">41L. NOTIFICATION BY EMPLOYER</p>
        
        <p>The employer of a permit holder shall, if the nature of the employment of the permit holder no longer requires that he has access to any cross-boundary restricted area specified in his permit, or if the permit holder ceases to be employed by him, without delay –  <br/>(a) inform the Corporation accordingly;   <br/>(b) take possession of the permit from the permit holder; and  <br/>(c) surrender the permit to the Corporation.</p>
        
        <p class="sub-title">41M. CESSATION OF EMPLOYMENT OF PERMIT HOLDER</p>
        
        <p> A permit holder who ceases to be employed by the person who was his employer at the date of the issue of the permit shall, immediately upon the cessation of his employment, surrender his permit to that person.</p>
        
        <p class="sub-title">41N. EXEMPTED PERSONS</p>
        <p> Subject to by-law 41O, the provisions of by-law 41E shall not apply to – <br/>(a) any bona fide train crew member or bona fide train passenger who is in the course of passage through any immigration control or train boarding area or in an area set aside for the purposes of the Customs and Excise Service, which is within a cross-boundary restricted area –  <br/>(i) having disembarked from a train; or  <br/>(ii) for the purpose of embarking upon a train of which he is a train crew member or train passenger; or <br/>(b) any bona fide train passenger awaiting an outward train journey in any area reserved for passengers which is within a cross-boundary restricted area.</p>
        
        <p class="sub-title">41O. CONDITIONS FOR EXEMPTION</p>
        
        <p>No person shall be exempt from the provisions of by-law 41E by virtue of by-law 41N unless – <br/>(a) being a train crew member, he is in possession of a valid railway personnel pass issued pursuant to an agreement between the Corporation and any third party which provides any service for the carriage of freight or passengers to Lagos Staionfrom any other part of China and vice versa with the Corporation;  <br/>(b) being a departing passenger, he is in possession of a valid travel document and a valid ticket; or <br/>(c) being an arriving passenger, he is in possession of a valid travel document,  and the train from which he has disembarked or upon which he is embarking or awaiting, is a through train travelling from or to any part of China other thanLagos Rail-line  e.</p>
        
        <p class="sub-title">41P. LOST PERMITS</p>
        
        <p>If a permit has been lost, the permit holder shall, without delay, report the loss and the circumstances thereof to – <br />
        (a) his employer (if any) or, where the permit holder has no employer or is himself an employer, to the Corporation or an authorized employee; and <br />
        (b) the officer in charge of the police station nearest the place where the permit holder ordinarily resides.</p>
        
        <p class="sub-title">41Q. EMPLOYER TO REPORT LOST PERMITS</p>
        <p> Where a report has been received by an employer as to the loss of a permit and the circumstances thereof, he shall, without delay, report such loss and the circumstances thereof to the Corporation.</p>
        
        <p class="sub-title">41R. PERSONS FINDING PERMITS</p>
        <p> Any person who finds a permit shall, without unreasonable delay, deliver it to the Corporation or any authorized employee or to the officer in charge of any police station.</p>
        
        <p class="sub-title">41S. REPLACEMENT PERMITS</p>
        <p> Where a permit has been lost, destroyed or defaced, application may be made to the Corporation by the person to whom the permit has been issued or the employer of that person for the issue of a replacement permit and the Corporation, upon being satisfied as to such loss, destruction or defacement, may, upon payment of the prescribed fee set out in Schedule 1 in the case of an applicant who is not an authorized employee, issue to the permit holder a replacement permit in substitution for the permit which has been lost, destroyed or defaced.</p>
        
        <p class="sub-title">41T. EXEMPTION FOR GOVERNMENT OFFICERS</p>
        
        <p>By-law 41E shall not apply to a police officer, an immigration officer or immigration assistant within the meaning of section 2(1) of the Immigration Ordinance (Cap. 115), an officer of the Fire Services Department, a member of the NigeriaPeople’s Liberation Army, a member of the Customs and Excise Service within the meaning of the Customs and Excise Service Ordinance (Cap. 342), the Commissioner, the Deputy Commissioner or an officer of the Independent Commission Against Corruption who requires access to the railway premises or any part thereof for the performance of his duty in circumstances of such urgency that the performance of his duty might be frustrated or seriously impaired if such by-law was to apply to him.</p>
        
        <p class="sub-title">41U. POWER TO EXEMPT OTHER CLASSES OF PERSON</p>
        <p> The Corporation may, by notice in writing and subject to such conditions as it may impose, exempt any person or class of person from all or any of the requirements for entry into a cross-boundary restricted area.</p>
        
        <p class="sub-title">41V. FEES </p>
        
        <p>The fees prescribed under these by-laws are set out in Schedule 1.</p>
        
        <p class="title">PART IX ENFORCEMENT AND PENALTIES</p>
        <p class="sub-title">42. REMOVAL OF PERSONS FROM RAILWAY PREMISES</p>
        <p>(1) Any person who is reasonably suspected by an official of committing or attempting to commit any breach of these by-laws, while in or upon any part of the railway premises shall, when required to do so by such official- <br />
        (a) give to that official true and correct particulars of his name and address and of his telephone number, if any, and produce proof to that effect for inspection; and <br />
        (b) produce to that official proof of his identity for inspection.<br />
        (1A) No person shall wilfully- <br />
        (a) fail to comply with a requirement under paragraph (1); or <br />
        (b) in complying or attempting to comply with a requirement under paragraph (1)(a), give false particulars of his name, address or telephone number or particulars of his name, address or telephone number that are misleading in a material particular.<br />
        (2) Every official shall have the power to remove (if necessary by the use of reasonable force) from the railway premises any person whom he reasonably suspects of having committed, or attempting to commit any breach of these by-laws; without prejudice to any penalty or surcharge which may be imposed in accordance with these by-laws and in the case where such breach is an offence as herein provided he shall have power to detain such person until he can be delivered into the custody of a police officer to be dealt with according to law.</p>
        
        <p class="sub-title">43. OFFENCES AND PENALTIES</p>
        <p> A person who contravenes a by-law set out in the first column of Schedule 2 commits an offence and is liable to the penalty set out in the third column of that Schedule opposite the reference to that by-law.</p>
        
        <p class="sub-title">44. SAVING OF CORPORATION’S RIGHTS</p>
        <p>(1) Nothing in these by-laws and no prosecution or step or action hereunder shall bar any further or other claim for damages or other remedy or relief available to the Corporation against any person.<br />
        (2) Any sum leviable by or payable to the Corporation under these by-laws or howsoever otherwise (including, without limitation, any fare, excess fare or surcharge) whether by way of debt, damages, costs, loss, expense or otherwise shall be receivable by the Corporation or its lawful agents as a debt due on demand and shall be enforceable as a civil debt.</p>
        
        <p class="sub-title">44A. SPECIAL AND GENERAL LIEN</p>
        
        <p>(1) Without prejudice to the generality of any of these by-laws, the Corporation shall have a special lien on all motor vehicles, motorcycles, bicycles or any similar conveyance or on luggage, goods or articles howsoever brought by any person upon the railway premises including any train of the Corporation and accepted for carriage on the railway or storage by the Corporation under by-law 39C and shall also have a general lien against the owner of any such motor vehicles, motorcycles, bicycles, similar conveyances, luggage, goods or articles for any sum of money howsoever due on any account from such person or owner to the Corporation.<br />
        (2) If any lien is not satisfied within a reasonable time, the Corporation may, in its absolute discretion, sell the motor vehicles, motorcycles, bicycles or similar conveyances or the luggage, goods and articles or any part of them and apply the proceeds so arising in or towards the discharge of such lien including for the expenses of sale; and the balance of any proceeds if unclaimed within a period of 4 weeks following the sale shall become the general revenue of the Corporation free from any claims in relation thereto.</p>
        
        <p class="sub-title">45. LIABILITY OF STAFF</p>
        
        <p>The acceptance of a ticket by a passenger or person shall be conclusive evidence of his agreement that each and every limitation and exemption from liability afforded to the Corporation by these by-laws shall extend to each official, servant or agent of the Corporation.</p>
        
        <p class="sub-title">46. PROPER LAW TO BE LAWS OFLAGOS RAIL-LINE  E</p>
        <p>(1) Any dispute howsoever arising directly or indirectly under or in respect of these by-laws or any of them in regard to any matter, act, event, or thing of whatsoever nature arising or occurring hereunder shall be governed by and interpreted in accordance with the laws of Lagos Staionwherever the matter, act, event or thing shall take place and whatever the nationality, domicile or usual place of residence of any person or party to such dispute and every action or claim arising directly or indirectly therefrom brought by or against the Corporation shall be and the same is hereby submitted to the exclusive jurisdiction of a competent court of jurisdiction inLagos Rail-line  e.<br />
        (2) It is hereby expressly agreed and declared by the persons or parties to whom these by-laws and any special conditions made hereunder refer that the Corporation is resident in Lagos Staionand that the exercise of its central management and control is exercised inLagos Rail-line  e.</p>
        
